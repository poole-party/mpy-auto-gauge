class Temperature:
    @staticmethod
    def lookup(value, units='c'):
        if units == 'f':
            units_key = 1
        else:
            units_key = 0

        value_str = str(value)
        subset_key = int(value_str[:2])
        if subset_key < 15 or len(value_str) < 5:
            return 0

        subset = TEMP_LOOKUP[subset_key]
        last_value_key = None
        for value_key in subset:
            if value_key == value:
                return subset[value_key][units_key]
            else:
                last_value_key = value_key
                if value_key > value:
                    if (value_key - value) < math.fabs(value - value_key):
                        return subset[value_key][units_key]
                    else:
                        return subset[last_value_key][units_key]

        return subset[last_value_key][units_key]
